public class Bird {
    // Unknown label is capitalized
    public String label;
    public double length;
    public double width;
    public double wingSpan;
    public double wingAngle;

    public Bird(String label, double length, double width, double wingSpan, double wingAngle) {
        this.label = label;
        this.length = length;
        this.width = width;
        this.wingSpan = wingSpan;
        this.wingAngle = wingAngle;
    }
}
